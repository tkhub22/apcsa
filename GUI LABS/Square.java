
public class Square 
{
	private int number;
	private boolean value;
	
	public Square(int number, boolean value)
	{
		this.number = number;
		this.value = value;
	}
	
	
	public int getNumber()
	{
		return this.number;
	}
	
	
	public boolean getValue()
	{
		return this.value;
	}
	
	
	public void setValue(boolean value)
	{
		this.value = true;
	}
}